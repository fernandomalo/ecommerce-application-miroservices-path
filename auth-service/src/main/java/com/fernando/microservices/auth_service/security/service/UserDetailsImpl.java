package com.fernando.microservices.auth_service.security.service;

import java.util.Collection;
import java.util.List;

import org.jspecify.annotations.Nullable;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import com.fernando.microservices.auth_service.entity.UserInfo;

public class UserDetailsImpl implements UserDetails{
    
    private Long id;
    private String email;
    private String password;
    private Collection<? extends GrantedAuthority> authorities;

    public UserDetailsImpl(Long id, String email, String password, Collection<? extends GrantedAuthority> authorities) {
        this.id = id;
        this.email = email;
        this.password = password;
        this.authorities = authorities;
    }

    public static UserDetails build(UserInfo user) {
        GrantedAuthority authority = new SimpleGrantedAuthority(user.getRole().getRole().name());

        return new UserDetailsImpl(
            user.getId(), 
            user.getEmail(),
            user.getPassword(), 
            List.of(authority)
        );
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return authorities;
    }

    @Override
    public String getPassword() {
        return password;
    }

    @Override
    public String getUsername() {
        return email;
    }

    public Long getId() {
        return id;
    }
    
}
